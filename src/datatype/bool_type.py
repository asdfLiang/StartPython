# 其他类型转bool
print(f"整数0转布尔型：{bool(0)}")
print(f"整数1转布尔型：{bool(1)}")
print(f"整数5转布尔型：{bool(5)}")

print(f"字符串'a'转布尔型：{bool("a")}")
print(f"字符串''转布尔型：{bool("")}")
print(f"字符串'true'转布尔型：{bool("true")}")
print(f"字符串'false'转布尔型：{bool("false")}")
print(f"字符串'True'转布尔型：{bool("True")}")
print(f"字符串'False'转布尔型：{bool("False")}")

print(f"浮点数0.0转布尔型：{bool(0.0)}")
print(f"浮点数5.3转布尔型：{bool(5.3)}")

print(f"列表[]转布尔型：{bool([])}")
print(f"列表[1, 2]转布尔型：{bool([1, 2])}")
